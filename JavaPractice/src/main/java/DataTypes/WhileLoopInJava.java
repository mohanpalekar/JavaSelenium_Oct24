package DataTypes;

public class WhileLoopInJava {
	
	public static void main(String[] args) {
		
		//condition
		
		int i = 1;
		
		while(i<11) {
			
			System.out.println(i);
			
			i++;
		}
		
		int [] arr = {12,545,86,43,23,21};
		
		int k =0;
		
		while(k<arr.length) {
			System.out.println(arr[k]);
			k++;
		}
	}

}
