package DataTypes;

public class WrapperClass {
	
	public static void main(String[] args) {
		
		int i = 0;
		
		System.out.println(i);
		
		Integer j = 123;
		
		System.out.println(j);
		
		Short s = 12;
		
		Byte b = 23;
		
		Long L = 13122371313l;
		
		System.out.println(L);
		
		Boolean B = true;
		
		Character ch = 'c';
		
		String str = "Hey There";
		
		System.out.println(str.toUpperCase());
		
		
	}

}
