package LoppsInJava;

public class ForLoop {

	public static void main(String[] args) {

		int [] arr = new int[5];

		arr[0] = 10;
		arr[1] = 20;
		arr[2] = 23;
		arr[3] = 8;
		arr[4] = 9;

		//start
		//condition --> True
		//	increment or decrement

		for(int j=0; j<=4; j++) {
			System.out.println(arr[j]);
		}

	}

}
