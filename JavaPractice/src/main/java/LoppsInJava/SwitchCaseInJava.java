package LoppsInJava;

public class SwitchCaseInJava {

	public static void main(String[] args) {


		int i = 0;

		switch(i) {

		case 1:
			System.out.println("One");
			break;

		case 2:
			System.out.println("Two");
			break;

		case 3:
			System.out.println("Three");
			break;

		default:
			System.out.println("0 or greatter than 3");
			
		}
		
		

	}

}
